# Вариант 2
def my_func_2(a, n):
    """
    Возведение числа a в степень n.

    :param int|float a: число, которое возводим в степень
    :param int n: значение степени
    :return:
    """
    result = 1
    for i in range(abs(n)):
        result *= a
    if n < 0:
        return 1 / result
    return result


print(my_func_2(2, 10))
print(my_func_2(2, 3))
print(my_func_2(2, -3))
