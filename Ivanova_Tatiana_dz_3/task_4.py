# Вариант 1
def my_func(a, n):
    """
    Возведение числа a в степень n.

    :param int|float a: число, которое возводим в степень
    :param int n: значение степени
    :return:
    """
    result = a ** abs(n)
    if n < 0:
        return 1 / result
    return result


print(my_func(2, 10))
print(my_func(2, 3))
print(my_func(2, -3))
